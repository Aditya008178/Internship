# Arithmetic Module
# In mathops.py, define custom add, subtract, multiply, and divide functions.


def add(a, b):
    print(f"Addition: {a+b}") 
  
def subtract(a, b):
    print(f"Subtraction: {a-b}")

def multiply(a, b):
    print(f"Multiplication: {a*b}")

def divide(a, b):
    print(f"Division: {a/b}") 